     private void cadastrarClientes() {
        String sql = "insert into clientes(nomeClientes,emailClientes,telefoneCliente,dataNascimentoCliente,sexoCliente,enderecoCliente,cepCliente,cpfCliente) values(?,?,?,?,?,?,?,?)";
        conexao = new ClasseConexao().conectorBD();
        
        try {
            //prepara e pega os dados do cadastro
            pst = conexao.prepareStatement(sql);
            pst.setString(1, nomeCliente.getText());//nome
            pst.setString(2, sexoCliente.getSelectedItem().toString());//sexo
            pst.setString(3, cpfCliente.getText());//cpf

            //converter data dd/MM/yyyy para yyyy/MM/dd
            String dia = dataDeNascimentoCliente.getText().substring(0, 2);
            String mes = dataDeNascimentoCliente.getText().substring(3, 5);
            String ano = dataDeNascimentoCliente.getText().substring(6);
            String dataNascConv = ano + "-" + mes + "-" + dia;

            pst.setString(4, dataNascConv);//dataNascimento
            pst.setString(5, enderecoCliente.getText());//endereco
            pst.setString(6, emailCliente.getText());//email
            pst.setString(7, telefoneCliente.getText());//telefone
            pst.setString(8, cepCliente.getText());//cep

            //validacao dos campos obrigatorios
            if (nomeCliente.getText().isEmpty() || cpfCliente.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Preencha Todos os Campos Obrigatorios");
            } else {

                //atualiza a tabela cliente com os dados do formulario
                int adicionado = pst.executeUpdate();
                if (adicionado > 0) {
                    JOptionPane.showMessageDialog(null, "Cliente Cadastrado Com Sucesso");

                    nomeCliente.setText(null);//nome
                    cpfCliente.setText(null);//cpf
                    dataDeNascimentoCliente.setText(null);//dataNascimento
                    enderecoCliente.setText(null);//endereco
                    emailCliente.setText(null);//email
                    telefoneCliente.setText(null);//telefone
                    cepCliente.setText(null);//cep
                }
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }

    }

